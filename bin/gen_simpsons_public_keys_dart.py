#!/usr/bin/env python3
"""Reads ../simpsonsPublicKeys.json and writes lib/dev/simpsons_public_keys.dart."""
import json, os

script_dir = os.path.dirname(os.path.abspath(__file__))
keys_path = os.path.join(script_dir, '..', '..', 'simpsonsPublicKeys.json')
out_path = os.path.join(script_dir, '..', 'lib', 'dev', 'simpsons_public_keys.dart')

with open(keys_path) as f:
    keys = json.load(f)

with open(out_path, 'w') as f:
    f.write('// Generated by createSimpsonsContactData.sh — do not edit.\n\n')
    f.write('final Map<String, dynamic> kSimpsonsPublicKeys = ')
    f.write(json.dumps(keys))
    f.write(';\n')

print(f'Written: {out_path}')
