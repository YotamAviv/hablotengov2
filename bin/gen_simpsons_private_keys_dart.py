#!/usr/bin/env python3
"""Reads ../simpsonsPrivateKeys.json and writes lib/dev/simpsons_private_keys.dart.

The output file is git-ignored because it contains private keys.
"""
import json, os

script_dir = os.path.dirname(os.path.abspath(__file__))
keys_path = os.path.join(script_dir, '..', '..', 'simpsonsPrivateKeys.json')
out_path = os.path.join(script_dir, '..', 'lib', 'dev', 'simpsons_private_keys.dart')

with open(keys_path) as f:
    keys = json.load(f)

with open(out_path, 'w') as f:
    f.write('// GENERATED — do not commit (contains private keys).\n\n')
    f.write('// ignore_for_file: constant_identifier_names\n')
    f.write('// Generated by createSimpsonsContactData.sh — do not edit.\n\n')
    f.write('final Map<String, dynamic> kSimpsonsPrivateKeys = ')
    f.write(json.dumps(keys))
    f.write(';\n')

print(f'Written: {out_path}')
