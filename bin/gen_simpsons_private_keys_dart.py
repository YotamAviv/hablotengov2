#!/usr/bin/env python3
"""Reads ../simpsonsPrivateKeys.json (identity + nerdster keys) and optionally
../simpsonsHabloKeys.json (hablotengo delegate keys), then writes
lib/dev/simpsons_private_keys.dart.

The output file is git-ignored because it contains private keys.
"""
import json, os

script_dir = os.path.dirname(os.path.abspath(__file__))
private_keys_path = os.path.join(script_dir, '..', '..', 'simpsonsPrivateKeys.json')
hablo_keys_path = os.path.join(script_dir, '..', '..', 'simpsonsHabloKeys.json')
out_path = os.path.join(script_dir, '..', 'lib', 'dev', 'simpsons_private_keys.dart')

with open(private_keys_path) as f:
    keys = json.load(f)

if os.path.exists(hablo_keys_path):
    with open(hablo_keys_path) as f:
        keys.update(json.load(f))

with open(out_path, 'w') as f:
    f.write('// GENERATED — do not commit (contains private keys).\n\n')
    f.write('// ignore_for_file: constant_identifier_names\n')
    f.write('// Generated by gen_simpsons_private_keys_dart.py — do not edit.\n\n')
    f.write('final Map<String, dynamic> kSimpsonsPrivateKeys = ')
    f.write(json.dumps(keys))
    f.write(';\n')

print(f'Written: {out_path}')
